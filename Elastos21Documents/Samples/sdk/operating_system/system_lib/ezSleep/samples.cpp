#include <stdio.h>
#include <elastos.h>
using namespace Elastos;

Int32 main(Int32 argc, Int8 *argv[])
{
    printf("Sleep for 1 second.\n");

    //the program will sleep for 1 second.
    CThread::Sleep(1000, NULL);

    printf("End sleep.\n");

    return 0;
}

//==========================================================================
// Output:
//
//  Sleep for 1 second.
//  End sleep.
