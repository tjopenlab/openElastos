// _strset.cpp:this progeam use _strset to sets all the characters of string to '*'


#include <string.h>
#include <stdio.h>

int main()
{
    char string[] = "Fill the string with something";
    printf("Before: %s\n", string);
    _strset(string, '*');
    printf("After:  %s\n", string);
    return 0;
}


