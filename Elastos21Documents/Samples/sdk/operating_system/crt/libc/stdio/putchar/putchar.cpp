/* putchar.cpp: This program writes characters from
 * buffer to screen.
 */

#include <stdio.h>

int main()
{
    char  buffer[] = "Testing putwchar.";
    char  *p;

    p = buffer;
    while (*p) {
        putchar(*p);
        p++;
    }
    putchar('\n');

    return 0;
}


/*
Output

    Testing putwchar.
*/
