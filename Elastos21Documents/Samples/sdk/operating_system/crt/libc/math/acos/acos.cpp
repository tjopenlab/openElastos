/* acos.cpp: This program prompts uses acos to calculates the arccosine.
 */

#include <math.h>
#include <stdio.h>
#include <stdlib.h>
#include <errno.h>

int main()
{
    double x = 0.32696, y;

    y = acos(x);
    printf("Arccosine of %f = %f\n", x, y);

    return 0;
}


