/* floor.cpp: This example displays the largest integers
 * less than or equal to the floating-point values 2.8
 * and -2.8.
 */

#include <math.h>
#include <stdio.h>

int main()
{
    double y;

    y = floor(2.8);
    printf("The floor of 2.8 is %f\n", y);
    y = floor(-2.8);
    printf("The floor of -2.8 is %f\n", y);

    return 0;
}

/*
Output

    The floor of 2.8 is 2.000000
    The floor of -2.8 is -3.000000
*/
