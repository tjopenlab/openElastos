/* pow.cpp:This program uses pow to computes x raised to the power of y
 */
#include <math.h>
#include <stdio.h>

int main()
{
   double x = 2.0, y = 3.0, z;

   z = pow(x, y);
   printf("%.1f to the power of %.1f is %.1f\n", x, y, z);

   return 0;
}


/*
Output

    2.0 to the power of 3.0 is 8.0
*/
