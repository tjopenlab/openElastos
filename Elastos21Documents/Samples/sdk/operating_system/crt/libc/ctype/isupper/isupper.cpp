/* isupper.cpp: This program uses isupper to verify c
 * whether it is a uppercase character.
 */

#include <stdio.h>
#include <ctype.h>

int main()
{
    int c = 67;
    if (isupper(c)) {
        printf("the character is an uppercase letter !\n");
    }
    else {
        printf("the character is not an uppercase letter !\n");
    }

    return 0;
}

/*
Output

    the character is an uppercase letter !
*/
