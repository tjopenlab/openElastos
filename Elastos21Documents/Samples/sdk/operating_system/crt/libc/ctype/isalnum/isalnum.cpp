/* isalnum.cpp: This program uses isalnum to verify c
 * whether it is an alphanumeric(A ¨C Z£¬a ¨C z or 0 ¨C 9).
 */

#include <stdio.h>
#include <ctype.h>

int main()
{
    int c = 100;
    if (isalnum(c)) {
        printf("it is a particular representation"
            " of an alphanumeric character.\n");
    }
    else {
        printf("it is not a particular representation "
                "of an alphanumeric character.\n");
    }

    return 0;
}

/*
Output

    it is a particular representation of an alphanumeric character.
*/
