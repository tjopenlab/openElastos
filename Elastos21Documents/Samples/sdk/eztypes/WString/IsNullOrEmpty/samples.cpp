#include <elastos.h>
using namespace Elastos;

Int32 main()
{
    WString as;
    if (as.IsNullOrEmpty()) {
        CConsole::WriteLine("as is null or empty.");
    }
    else {
        CConsole::WriteLine("as is not null or empty.");
    }

    WString sz(L"");
    if (sz.IsNullOrEmpty()) {
        CConsole::WriteLine("sz is null or empty.");
    }
    else {
        CConsole::WriteLine("sz is not null or empty.");
    }

    return 0;
}

//==========================================================================
//Output:as is null or empty.
//       sz is null or empty.
//==========================================================================
